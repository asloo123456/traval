<template>
 <div class="app-container">
    <!--容器负责显示所有组件 Home-->
    <router-view v-if="isRouterAlive"></router-view>
 </div>
</template>
<script>
// 原地刷新
export default {
  name: 'App',
  provide() { // 注册一个方法
    return {
      reload: this.reload
    }
  },
  data() {
    return {
      isRouterAlive: true
    }
  },
  methods: {
    reload() {
      this.isRouterAlive = false
      this.$nextTick(function() {
        this.isRouterAlive = true
      })
    }
  }
}
</script>
